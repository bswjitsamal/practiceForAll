package restassured.automation.Pojo;

public class ETRoles_PortalRoles_Pojo {
	private String portalRoleId;
	private String UserID;
	private String name;
	private String[] permissions;
	
	
	
	public String getPortalRoleId() {
		return portalRoleId;
	}
	public void setPortalRoleId(String portalRoleId) {
		this.portalRoleId = portalRoleId;
	}
	public String getUserID() {
		return UserID;
	}
	public void setUserID(String userID) {
		UserID = userID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String[] getPermissions() {
		return permissions;
	}
	public void setPermissions(String[] permissions) {
		this.permissions = permissions;
	}
	
	

}
