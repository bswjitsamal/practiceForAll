package restassured.automation.Pojo;

public class ETRoles_PortalRoles_Pojo {
	private String portalRoleId;
	private String UserID;
	private String name;
	private String[] permission;
	
	
	
	public String getPortalRoleId() {
		return portalRoleId;
	}
	public void setPortalRoleId(String portalRoleId) {
		this.portalRoleId = portalRoleId;
	}
	public String getUserID() {
		return UserID;
	}
	public void setUserID(String userID) {
		UserID = userID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String[] getPermission() {
		return permission;
	}
	public void setPermission(String[] permission) {
		this.permission = permission;
	}
	
	

}
